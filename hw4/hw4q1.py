def main():
    question = int(input())

    if question == 1:
        print('The answer to question 1 is h')
    elif question == 2:
        print('The answer to question 2 is a')
    elif question == 3:
        print('The answer to question 3 is h')
    elif question == 4:
        print('The answer to question 4 is i')
    elif question == 5:
        print('The answer to question 5 is b')
    elif question == 6:
        print('The answer to question 6 is v')
    else:
        print('Error')
        exit()